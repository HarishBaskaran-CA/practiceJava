package org.Apr9;

public class Print20Numbers {

    // Print first 20 numbers
    // Print the next 20 numbers starting from any given number
    // Convert this logic into a method
    // call that method using an object

    public void callingNumbers(int number) {
        for (int i = number; i < number + 20; i++) {
            System.out.print(i+",");
            System.out.println();
        }
    }
}



